# Erstellen Sie ein Programm, dass für eine Zahl ausgibt, ob Sie durch 2, 3, 5 oder 7 teilbar sind.

x = 45

if x % 2 == 0:
    print(str(x) + " ist durch 2 teilbar.")

if x % 3 == 0:
    print(str(x) + " ist durch 3 teilbar.")

if x % 5 == 0:
    print(str(x) + " ist durch 5 teilbar.")

if x % 7 == 0:
    print(str(x) + " ist durch 7 teilbar.")
